import mongoose from "mongoose";


export const userSchema = mongoose.Schema({
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    lastLogin: {
        type: Date,
        default: Date.now,
    },
    isVerified: {
        type: Boolean,
        default: false,
    },
    resetPasswordToken: String,
    resetPasswordExpiressAt: Date,
    verificationToken: String,
    verificationTokenExpiressAt: Date
}, { timestamps: true })

export const User = mongoose.model("User", userSchema);